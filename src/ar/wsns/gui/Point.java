package ar.wsns.gui;

/**
 * @author Manuel Argüelles
 */
public class Point {

    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() { return x; }

    public int getY() { return y; }

    public void setX(int x) { this.x = x; }

    public void setY(int y) { this.y = y; }

    /*
     * Returns if a Point is contained into a rectangle defined by the offset
     * parameter and centered on this Point.
     */
    public boolean contains(Point p, int offset) {
        return (p.getX() > x-offset/2 && p.getX() < x+offset/2 &&
                p.getY() > y-offset/2 && p.getY() < y+offset/2);
    }

}